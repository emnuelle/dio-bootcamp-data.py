### métodos úteis da classe string

curso = "python"

#  Maiúscula(".upper()"), 
print(curso.upper())

# minúscula(".lower()") 
print(curso.lower())

# título(".title()")
print(curso.title())

#  Eliminando espaços em branco: Esquerda e direita (".strip()")
print(curso.strip())

# esquerda(".lstrip()")
print(curso.lstrip())

# direita(".rstrip()")
print(curso.rstrip())

#  Junções(".join()")  
print(".".join(curso))

# centralização(".center()")
print(curso.center(10, "#"))
